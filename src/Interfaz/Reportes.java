    private void btnSearch2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearch2ActionPerformed
        producto.clear();
        Logica.Logica.reporte3();
        String matriz [][] = new String[producto.size()][4];
        int m = 0;
        for (int i = 0; i < producto.size(); i+=4) {
            if(12<Integer.parseInt(producto.get(i+2).toString())){
                matriz[m][0] = producto.get(i).toString();
                matriz[m][1] = producto.get(i+1).toString();
                matriz[m][2] = producto.get(i+2).toString();
                matriz[m][3] = producto.get(i+3).toString();
                m+=1;
            }
        }
        tblProductos2.setModel(new javax.swing.table.DefaultTableModel(
            matriz,
            new String [] {
                "Nombre", "Sexo", "Edad", "Producto"
            }
        ));
    }//GEN-LAST:event_btnSearch2ActionPerformed

