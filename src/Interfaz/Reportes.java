    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        producto.clear();
        try {
            Logica.Logica.reporte1(dateMin.getDate(), dateMax.getDate());
        } catch (ParseException ex) {
            Logger.getLogger(Reportes.class.getName()).log(Level.SEVERE, null, ex);
        }
        String matriz [][] = new String[producto.size()][1];
        for (int i = 0; i < producto.size(); i++) {
            matriz[i][0] = producto.get(i).toString();
        }
        tblProductos.setModel(new javax.swing.table.DefaultTableModel(
            matriz,
            new String [] {
                "Nombre del Producto"
            }
        ));
    }//GEN-LAST:event_btnSearchActionPerformed

    private void btnSearch1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearch1ActionPerformed
        producto.clear();
        Logica.Logica.reporte2(boxTipe.getSelectedItem().toString());
        String matriz [][] = new String[producto.size()][3];
        int m = 0;
        for (int i = 0; i < producto.size(); i+=3) {
            matriz[m][0] = producto.get(i).toString();
            matriz[m][1] = producto.get(i+1).toString();
            matriz[m][2] = producto.get(i+2).toString();
            if((i+3)==producto.size()){
                break;
            }
            m+=1;
        }
        tblProductos1.setModel(new javax.swing.table.DefaultTableModel(
            matriz,
            new String [] {
                "Nombre del Producto", "Precio", "Tipo"
            }
        ));
    }//GEN-LAST:event_btnSearch1ActionPerformed

